#include <unistd.h>

int	main( int argc, char *argv[])
{
	int	i;
	char	*s;

	i = 0;
	s = argv[1];
	if (argc == 2)
	{
		while (s[i])
		{
			if (s[i] >= 'a' && s[i] <= 'z')
				s[i] = 'z' - s[i] + 'a';
			else if (s[i] >= 'A' && s[i] <= 'Z')
				s[i] = 'Z' - s[i] + 'A';
			write (1, &s[i], 1);
			i++;
		}
	}
	write (1, "\n", 1);
	return (0);
}
