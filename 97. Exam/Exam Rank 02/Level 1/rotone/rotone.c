#include <unistd.h>

int	main(int argc, char *argv[])
{
	char	*s;
	int	i;

	s = argv[1];
	i = 0;
	if (argc == 2)
	{
		while (s[i])
		{
			if (s[i] >= 'a' && s[i] <= 'y')
				s[i] += 1;
			else if (s[i] == 'z')
				s[i] = 'a';
			else if (s[i] >= 'A' && s[i] <= 'Y')
				s[i] += 1;
			else if (s[i] == 'Z')
				s[i] = 'A';
			write (1, &s[i], 1);	
			i++;
		}
	}
	write (1, "\n", 1);
}
