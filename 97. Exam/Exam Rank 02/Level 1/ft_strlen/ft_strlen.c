#include <stdio.h>

int	ft_strlen(char	*s)
{
	int	i;

	i = 0;
	while (s[i])
		i++;
	return (i);
}

int	main()
{
	char	c[] = "NAT";

	printf("%d\n", ft_strlen(c));
	return (0);
}
